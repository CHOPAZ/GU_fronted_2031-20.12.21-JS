const number = parseInt(prompt(`Введите количество яблок`));

function numberFruits(count, one, two, three) {
    // делаем проверку, для двузначных числе, если больше 10 шт.
    const a = count % 100; 
    // проверка если в диапазоне от 1 до 10 
    const b = a % 10;

    // условиt для `яблок`
    if (a >= 5 && a <= 20) {
        return three;
    }
    // условие для `яблока`
    if (b > 1 && b < 5 ) {
        return two;
    }
    // условие для `яблоко`
    if (b === 1){
        return one;
    }
    return three;

}

