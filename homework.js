let a = 1, b = 1, c, d;

// Переменной `с` присваивается значение переменной `а`, увеличенное на 1 при помощи оператора инкримента (++) = 2. И сама переменная `а` увеличится на 1. 
// a = 2, b = 1, c = 2, d;
c = ++a;  
console.log(c);

// Переменной `d` присваивается значение переменной `b` - 1. Оператор инкримента (++) стоит после переменной `b`, поэтому `b` увеличивается на 1, и возвращает значение переменной `b` до увеличения.
// a = 2, b = 2, c = 2, d = 1;
d = b++;
console.log(d);

// Переменная "c" равна 2 плюс 3 равно 5. Переменная `a` = 2, она увеличивается на один за счет (++) и равна 3.
// a = 3, b = 2, c = 5, d;
